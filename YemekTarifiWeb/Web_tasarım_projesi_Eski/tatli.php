                <img class="img1" src="sutlac.jpg" alt="Sütlaç">
                <p class="p1">Malzemeler:</p>
                <ul>
                    <li>1 lt süt </li>
                    <li>2 çay bardağı pirinç</li>
                    <li>1 litre su</li>
                    <li>3 yemek kaşığı pirinç unu</li>
                    <li>1,5 -2 su bardağı toz şeker (eğer çok şekerli sevmiyorsanız 1,5 bardak kullanabilirsiniz)</li>
                    <li>1 su bardağı süt</li>
                    <li>tarçın</li>
                    
                </ul>
                <p><p class="p1">Hazırlanışı:</p>Sütlaç yapmak için öncelikle pirinci yıkayıp su ile ateşe koyun.
                Pirinçler uzayıp suyu çekene kadar kaynatın. Ocağı çok yüksek ateşte açarak pirinçleri yakmamaya dikkat edin.
                Ardında soğuk sütü ekleyin. 1-2 defa karıştırıp, kaynamasını bekleyin.
                Bu arada bir kasede pirinç ununu 1 su bardağı soğuk süt ile ezin. Topak kalmamasına mutlaka dikkat edin.
                Tencerede kaynamakta olan sütten 1-2 kepçe alıp kaseye ekleyin. (pirinç unu ılınmış olmalı).
                Pirinç ununu tencereye ekleyin, ara sıra karıştırarak 10 dakika kadar pişirin.
                Toz şekeri ilave edip karıştırın ve 1-2 taşım daha kaynatın.
                Sütlacı kaselere paylaştırın.
                Soğuyunca sütlaçların üzerlerine tarçın serperek servis edebilirsiniz. Afiyet olsun.</p>
