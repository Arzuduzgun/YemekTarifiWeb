                <img class="img1" src="sekerpare.jpg" alt="Şekerpare">
                <p class="p1">Malzemeler:</p>
                <ul>
                    <li> 125 gram oda ısısında margarin</li>
                    <li>Yarım su bardağı pudra şekeri</li>
                    <li>3 çorba kaşığı silme irmik</li>
                    <li>1 adet yumurta</li>
                    <li>2,5 su bardağı un</li>
                    <li>1 paket vanilya</li>
                    <li>1 çay kaşığı silme kabartma tozu</li>
                    <li>1 çay bardağı fındık</li>
                    <p>Şerbeti için:</p>
                    <li> 2 su bardağı şeker</li>
                    <li>3 su bardağı su</li>
                    <li>4-5 damla limon suyu</li>
 
                </ul>
                <p><p class="p1">Hazırlanışı:</p>Karıştırma kabına yağı, pudra şekerini koyup karıştırıyoruz.
                Yumurtayı ilave edip tekrar karıştırıyoruz
                İrmiği, kabartma tozunu, vanilyayı ve unu azar azar ilave ederek kulak memesi yumuşaklığında hamur elde ediyoruz.
                Hamurdan cevizden biraz büyük parçalar kopartıp elimizle yuvarlıyoruz.
                Üzerini çok hafif düzleyip ortalarına birer bütün fındık içi yerleştiriyoruz.
                Yağlanmış fırın tepsisine diziyoruz.
                180 derece önceden ısıtılmış fırında üzeri altın sarısı rengini alana kadar yaklaşık 30 dakika pişiriyoruz.
                 Şerbet için derin tencereye suyu ve şekeri koyup yaklaşık 10-15 dakika arası pişiriyoruz.
                 İndirmeye yakın limon suyunu ekliyoruz 2-3 dakika daha pişiriyoruz.
                Pişen şerbeti ılınmaya bırakıyoruz.
                Pişen şekerpareleri fırından alıp 2 dakika kadar bekletip ılık şerbeti üzerine kepçe yardımı ile gezdiriyoruz.Afiyet olsun.</p>
