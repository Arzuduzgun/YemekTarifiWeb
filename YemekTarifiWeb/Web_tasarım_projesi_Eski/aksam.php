                Domateslerin kabuklarını soyarak, onları da küp küp doğrayın.
                Mantarların kabuklarını soyarak onları da ince ince dilimleyelim.
                Tencereye bir miktar sıvı yağı  koyup doğranan soğan, biber ve sarımsakları 1-2 dakika kavurun.
                Ardından üzerine S-salça ve pul biberi ekleyerek 1-2 dakika daha kavurun.
                İnce ince dilimlenmiş mantarları ilave edip karıştırın. Mantarlar suyunu salıp çekinceye kadar pişirmeye devam edin.
                Karıştırılarak bir süre pişirildikten sonra doğranmış domatesleri ve tuzu ekleyerek yemek suyunu çekip, mantarlar yumuşayana  kadar pişirin.
                Mantar sotemiz servise hazır.Afiyet olsun.</p>
