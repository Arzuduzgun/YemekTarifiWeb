                <p><p class="p1">Hazırlanışı:</p>Dolmalık biberlerimizi temizleyelim.
                Soğanları, domatesi küçük küçük doğrayalım.
                Domatesin kenarlarını biberlerin üstünü kapatmak için kullanacağız o yüzden önce kapatmak için kullanacaklarımızı kesersek daha iyi olur.
                İçini yemek harcında kullanabiliriz.
                Bir kaba hazırladığımız soğan, pirinç, salça, maydanoz, domates, karabiber, tuz, nane ve çok az sıvı yağı ekleyip iyice karıştıralım.
                Daha sonra bu harcı biberlere dolduralım.
                Harcı doldurduğumuz biberleri, tencereye dizelim.
                Harcı yaptığımız kabın içine sıcak su koyalım.
                O artık göz kararı olacak biberlerin yarısına kadar gelecek şekilde kabın içine koyduğumuz suya 1 yemek kaşığı salça ekleyelim ve eritelim.
                Bu karışımı biberlerin üstüne gelmeyecek şekilde tencereye, biberlerin yarısına gelene kadar dökelim.
                Kestiğimiz domateslerle biberlerin üstünü kapatalım.
                Daha sonra biberlerin üstüne çok az sıvı yağ veya zeytinyağı gezdirelim.
                Kısık ateşte yemeğimizi pişirelim.
                Afiyet olsun.</p>
